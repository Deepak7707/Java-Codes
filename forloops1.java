import java.io.*;
import java.util.*;

public class forloops1{

    public static void main(String[] args) {
           Scanner s= new Scanner(System.in);
        int n= s.nextInt();
        
        for(int i=0; i<=n; i+=7){
            
               
                System.out.print(i+" ");
            }
    
              
        }
        
    }